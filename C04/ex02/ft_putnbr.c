#include <unistd.h>

void	ft_putnbr(int nb)
{
	char narr[12];
	int	i;
	int	tempn;

	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	i = 0;
	while (nb > 0)
	{	
		tempn = nb % 10;
		narr[i] = tempn - '0';
		i++;
	}
	narr[i] = nb - '0';
	while (i > 0)
	{	
		i--;
		write (1, narr[i],1);
	}
}

	
