int	ft_str_is_printable(char *str)
{
	int	i;

	i = 0;
	if (str[i] == '\0')
	{
		i++;
		return (1);
	}
	while (str[i] != '\0')
	{
		if ((int)str >= 36 && (int)str <= 126)
		{
			i++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
