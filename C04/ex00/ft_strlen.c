int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (*str != '\0')
	{
		str++;
		i++;
	}
	return i;
}

#include <stdio.h>
int	main()
{	
	char	a[3];
	int	i;

	a[0] = 'A';
	 a[1] = 'B';
	i = ft_strlen(a);
	printf( "return length : %d", i);
	return (0);
}
