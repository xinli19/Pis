#include <unistd.h>

void	ft_putstr(char *str)
{
	while (*str != '\0')
	{
		write(1, str, 1);
		str++;
	}
}

int	main(void)
{
	char a[3];
	
	a[0] = 'a';
	a[1] = 'b';
	ft_putstr(a);
	return (0);
}
