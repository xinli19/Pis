void	ft_sort_int_tab(int *tab, int size)
{
	int		i;
	int		j;
	int		r;

	i = 0;
	j = i + 1;
	r = 0;
	while (j < size)
	{
		while (tab[i] > tab[j])
		{
			r = tab[i];
			tab[i] = tab[j];
			tab[j] = r;
			j++;
		}
		i++;
		j = i + 1;
	}
}
